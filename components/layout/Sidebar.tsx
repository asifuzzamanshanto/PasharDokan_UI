export default function Sidebar({ role, shopName }: SidebarProps) {
  const pathname = usePathname();
  const [collapsed, setCollapsed] = useState(false);

  const navItems = NAV_CONFIG[role] || [];
  const isAdminRole = role === "platform_admin";

  return (
    <aside
      className={cn(
        "hidden lg:flex flex-col bg-surface border-r border-border h-[calc(100vh-3.5rem)] sticky top-14 transition-all duration-200",
        collapsed ? "w-16" : "w-56"
      )}
    >
      {/* Header */}
      <div className="h-11 flex items-center px-4 border-b border-border">
        {!collapsed && (
          <span className="text-[11px] font-semibold text-text-tertiary uppercase tracking-wider">
            {isAdminRole ? "Platform Admin" : shopName || "Shop Workspace"}
          </span>
        )}
        <button
          onClick={() => setCollapsed(!collapsed)}
          className="ml-auto p-1 text-text-tertiary hover:text-text-primary hover:bg-surface-sunken rounded transition-colors cursor-pointer"
        >
          <svg className={cn("w-4 h-4 transition-transform", collapsed && "rotate-180")} fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 19.5 8.25 12l7.5-7.5" />
          </svg>
        </button>
      </div>

      {/* Nav */}
      <nav className="flex-1 p-2 space-y-0.5 overflow-y-auto">
        {navItems.map((item) => {
          const isActive = pathname === item.href || pathname.startsWith(item.href + "/");
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                "flex items-center gap-3 rounded-lg text-sm font-medium transition-all duration-150",
                collapsed ? "px-0 py-2.5 justify-center" : "px-3 py-2",
                isActive
                  ? "bg-primary-50 text-primary-700"
                  : "text-text-secondary hover:text-text-primary hover:bg-surface-sunken"
              )}
              title={collapsed ? item.title : undefined}
            >
              <span className={cn("shrink-0", isActive ? "text-primary-600" : "text-text-tertiary")}>
                {iconMap[item.icon] || <div className="w-[18px] h-[18px]" />}
              </span>
              {!collapsed && item.title}
            </Link>
          );
        })}
      </nav>

      {/* Footer */}
      <div className="p-3 border-t border-border">
        <Link
          href={isAdminRole ? "/" : "/"}
          className={cn(
            "flex items-center gap-2 text-xs text-text-tertiary hover:text-text-primary transition-colors",
            collapsed && "justify-center"
          )}
        >
          <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M10.5 19.5 3 12m0 0 7.5-7.5M3 12h18" />
          </svg>
          {!collapsed && "Back to Home"}
        </Link>
      </div>
    </aside>
  );
}
